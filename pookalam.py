import turtle

# Screen configuration
scrn = turtle.Screen()
scrn.bgcolor("black")
turtle.screensize(400,400)
scrn.setworldcoordinates(100,500,600,100)

# Turtle pen configuration
tur = turtle.Turtle()
tur.penup()
tur.speed("fastest")
tur.pensize(1)
tur.hideturtle()


def flower():
    
    # Background circle
    tur.goto(350,80)    
    tur.color("#FF6B00")
    tur.begin_fill()
    tur.circle(220)
    tur.end_fill()
    
    # Circle quadrants
    tur.goto(350,80)
    for i in range(4):
        tur.color(["#6D1022","#A5306B"][i%2])
        tur.begin_fill()
        tur.circle(220,360/4) 
        tur.end_fill()
    
    # Flower Petals
    tur.goto(350,260)
    tur.pendown()
    for i in range(16):
        tur.color(["#FF0000","#FFC700","#00FF00","#C4D9E0"][i%4])
        tur.begin_fill()
        tur.right(90)
        tur.circle(140, 90)
        tur.left(90)
        tur.circle(140, 90)
        tur.right(180)
        tur.end_fill()
        tur.circle(40, 24)
    tur.color("black")
    tur.penup()
    turtle.left(144)

def bg():
    # Circular background on the flower
    tur.pencolor("white")
    tur.goto(420,140)
    tur.color("#FF3D00")
    tur.begin_fill()
    tur.circle(180)
    tur.end_fill()
    tur.penup()
    
    # Circular background of Kathakali figure
    tur.goto(410,168)
    tur.color("#00020D")
    tur.begin_fill()
    tur.circle(150)
    tur.end_fill()
    tur.penup()
    
    # Square pattern behind kathakali figure
    tur.goto(350,300)
    tur.shape("square")
    tur.shapesize(13,13)
    for i in range(4):
        tur.left(22.5*i)
        tur.color(["white","yellow"][i%2])        
        tur.stamp()
        tur.right(22.5*i)
    
    # Green background of kathakali figure
    tur.goto(385,210)    
    tur.color("#57AB0E")
    tur.begin_fill()
    tur.circle(100)
    tur.end_fill()
    tur.penup()

def design():
    
    # Circular ring pattern - on outside circle
    tur.goto(420,140)
    tur.shape("circle")
    for i in range(110):
        tur.circle(180,360/110)
        tur.color(["#D80A0A","#B81A19","#B5A723","#EBE255"][i%4])
        tur.stamp()
    
    # Yellow white circular ring pattern - on inside circle
    tur.goto(410,170)
    tur.shape("turtle")
    for i in range(100):
        tur.circle(150,360/100)
        tur.color(["#F1DE6D","white"][i%2])
        tur.stamp()

